CREATE TABLE IF NOT EXISTS HOSPITAL (
    ID BIGINT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    NAME VARCHAR(100) NOT NULL,
    LEGAL_NAME VARCHAR(100),
    HOSPITAL_CODE VARCHAR(20) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR(255),
    TELEPHONE VARCHAR(20) NOT NULL,
    SECONDARY_TELEPHONE VARCHAR(20),
    EMAIL VARCHAR(100) NOT NULL,
    WEB_SITE VARCHAR(100),
    TAX_IDENTIFICATION_NUMBER VARCHAR(20) UNIQUE,
    LICENSE_NUMBER VARCHAR(50),
    BED_CAPACITY INT,
    FOUNDATION_YEAR INT,
    TIME_ZONE VARCHAR(50) DEFAULT 'America/La_Paz',
    LOGO LONGBLOB,
    IS_DEFAULT BOOLEAN NOT NULL DEFAULT FALSE,
    FK_ID_ADDRESS BIGINT,
    CREATED_BY VARCHAR(100),
    CREATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    LAST_MODIFIED_BY VARCHAR(100),
    LAST_MODIFIED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_HOSPITAL_ADDRESS FOREIGN KEY (FK_ID_ADDRESS) REFERENCES ADDRESS(ID)
);
COMMIT;
