CREATE TABLE IF NOT EXISTS PATIENT (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    ID_CARD VARCHAR(100) UNIQUE,
    PATIENT_STATUS VARCHAR(50) NOT NULL,
    OCCUPATION VARCHAR(100),
    NATIONALITY VARCHAR(100),
    DEGREE_OF_INSTRUCTION VARCHAR(100),
    FK_COUNTRY_OF_BIRTH BIGINT,
    FK_ROOM BIGINT,
    PHOTO LONGBLOB,
    FK_ID_INSURANCE BIGINT,
    FK_ID_NURSE BIGINT,
    ADDITIONAL_REMARKS TEXT,
    FK_HABITO_TOXICO BIGINT,
    FK_HABITO_FISIOLOGICO BIGINT,
    FK_HISTORIA_FAMILIAR BIGINT,
    BLOOD_GROUP VARCHAR(6),
    WEIGHT DECIMAL(5,2),
    HEIGHT DECIMAL(5,2),
    EMERGENCY_CONTACT_NAME VARCHAR(100),
    EMERGENCY_CONTACT_PHONE VARCHAR(20),
    FECHA_REGISTRO_PACIENTE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FECHA_ACTUALIZACION_PACIENTE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    TIENE_DISCAPACIDAD BOOLEAN,
    DETALLES_DISCAPACIDAD VARCHAR(250),
    EMBARAZADA BOOLEAN,
    SEMANAS_EMBARAZO INT,
    NUMERO_HIJOS INT,
    GRUPO_ETNICO VARCHAR(100),
    CONSTRAINT FK_PATIENT_COUNTRY FOREIGN KEY (FK_COUNTRY_OF_BIRTH) REFERENCES COUNTRY(ID),
    CONSTRAINT FK_PATIENT_ROOM FOREIGN KEY (FK_ROOM) REFERENCES ROOM(ID),
    CONSTRAINT FK_PATIENT_INSURANCE FOREIGN KEY (FK_ID_INSURANCE) REFERENCES INSURANCE(ID),
    CONSTRAINT FK_PATIENT_NURSE FOREIGN KEY (FK_ID_NURSE) REFERENCES NURSE(ID),
    CONSTRAINT FK_PATIENT_HABITO_TOXICO FOREIGN KEY (FK_HABITO_TOXICO) REFERENCES HABITO_TOXICO(ID),
    CONSTRAINT FK_PATIENT_HABITO_FISIOLOGICO FOREIGN KEY (FK_HABITO_FISIOLOGICO) REFERENCES HABITO_FISIOLOGICO(ID),
    CONSTRAINT FK_PATIENT_HISTORIA_FAMILIAR FOREIGN KEY (FK_HISTORIA_FAMILIAR) REFERENCES HISTORIA_FAMILIAR(ID)
);
COMMIT;